// 2 DIMENSIONAL ARRAY IN JAVA
// MUTABLE
public class Two_dim {
  public static void main(String[] args) {
    // Declare and initialize a 2D array
    int[][] arr = {
        {1, 2, 3},
        {4, 5, 6},
        {7, 8, 9}
    };

    System.out.println("Element at arr[0][0]: " + arr[0][0]); // Output: 1

  }  
}

